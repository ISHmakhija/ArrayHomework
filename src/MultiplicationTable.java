public class MultiplicationTable
{
    public static void main(String[] args)
    {
        //declaring integer
        int a = 1;

     // creation of do-while loop
        do
        {
             //printing tables by sout, in table format & increasing value of a.
            System.out.println(1*a +"\t"+ 2*a +"\t" + 3*a +"\t");
              a++;
        }
        while(a<=3);

    }

}
